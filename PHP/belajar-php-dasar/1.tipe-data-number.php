echo "Octal : ";
var_dump(0123);

echo "Binary : ";
var_dump(0b11111111);

echo "Hexadecimal : ";
var_dump(0x1A);

// Float untuk pecahan
echo "Floating Point :";
var_dump(1.234);

echo "Floating Point dengan E notation plus (1.2 x 1000) :";
var_dump(1.2e3);

